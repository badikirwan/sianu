    public void getData() {
        DefaultTableModel tbm = new DefaultTableModel();
        tbm.addColumn("ID");
        tbm.addColumn("NAMA");
        tbm.addColumn("PROFESI");
        tbm.addColumn("NO TELEPON");
        tbm.addColumn("ALAMAT");
        try {
            Connection con = (Connection)sianu.koneksi.Koneksi.getKoneksi();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from kader");
            while(rs.next()) {
                tbm.addRow(new Object[] {
                    rs.getString("id"), rs.getString("nama"),rs.getString("profesi"), rs.getString("no_kontak"),rs.getString("alamat")});
               tabel.setModel(tbm);
            }
        } catch(Exception e) {
            
        }
        int a = tbm.getRowCount();
        txtJumlah.setText(a+" Orang");
    }


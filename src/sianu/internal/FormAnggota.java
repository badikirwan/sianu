    public void autokode() {
        try {
            Connection con = sianu.koneksi.Koneksi.getKoneksi();
            Statement state = con.createStatement();
            String sql = "select max(ID) from kader";
            ResultSet rs = state.executeQuery(sql);
            while(rs.next()){
             int a = rs.getInt(1);
            anggota.txtID.setText("000"+Integer.toString(a+1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    

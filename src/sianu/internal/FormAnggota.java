    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
         try {
            int row = tabel.getSelectedRow();
            String tabel_klik=(tabel.getModel().getValueAt(row, 0).toString());
            Connection con =(Connection)sianu.koneksi.Koneksi.getKoneksi();
            Statement st = con.createStatement();
            ResultSet sql = st.executeQuery("SELECT * FROM kader WHERE id='"+tabel_klik+"'");
            if(sql.next()){
                anggota.txtID.setText(sql.getString("ID"));
                anggota.txtNama.setText(sql.getString("nama"));
                anggota.txtTmpLahir.setText(sql.getString("tmp_lahir"));
                String tgl = sql.getString("tgl_lahir");
                DateFormat format = new SimpleDateFormat("dd/MM/yyyy");
                Date date = format.parse(tgl);
                anggota.txtTglLahir.setDate(date);
                anggota.txtProfesi.setText(sql.getString("profesi"));
                anggota.txtAlamat.setText(sql.getString("alamat"));
                anggota.txtNoTlp.setText(sql.getString("no_kontak"));
                anggota.txtEmail.setText(sql.getString("email"));
                anggota.txtKhidup.setText(sql.getString("k_hidup"));
                anggota.txtPendFormal.setText(sql.getString("pend_formal"));
                anggota.txtPendNonFormal.setText(sql.getString("pend_nonformal"));
                anggota.txtKorganisasi.setText(sql.getString("k_organisasi"));
                anggota.txtRkader.setText(sql.getString("riwayat_kaderisasi"));
                anggota.txtFb.setText(sql.getString("fb"));
                anggota.txtTwitter.setText(sql.getString("twitter"));
                anggota.txtInstagram.setText(sql.getString("instagram"));
                String foto = sql.getString("foto");
                anggota.nm.setText(foto);
                String path = "D:/APP/image/";
                ImageIcon image = new ImageIcon(path+foto);
                Rectangle rect = anggota.gambar.getBounds();
                Image scaledimage = image.getImage().getScaledInstance(rect.width,rect.height,Image.SCALE_DEFAULT);
                image = new ImageIcon(scaledimage);
                anggota.gambar.setIcon(image);
            } 
        } catch (SQLException | ParseException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        } 
        anggota.show();
    }//GEN-LAST:event_tabelMouseClicked

